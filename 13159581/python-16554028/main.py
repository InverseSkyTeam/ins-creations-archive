from time import *
from xes.ext import *
a = input("请输入你要倒计时的时间（s）：")
b = int(a)
class a:
    def b(self):
        self.b = localtime()
        print("计时开始...")
    def c(self):
        self.c = localtime()
        self._calc()
        print("计时结束！")
    def _calc(self):
        self.lasted = []
        self.prompt = "总共运行了"
        for i in range(6):
            self.lasted.append(self.c[i] - self.b[i])
            self.prompt += str(self.lasted[i])
        print(self.prompt)
z = a()
z.b()
sleep(b)
z.c()
